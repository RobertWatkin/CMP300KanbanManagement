    {
        if (!request()->has('bucketid')) {
            return redirect()->route("board.index");
        }


        $bucket = Bucket::find(request()->bucketid)->first();
        $board = Board::find($bucket->board_id)->first();

        return view('card.create', ['bucket' => $bucket, 'board' => $board]);
